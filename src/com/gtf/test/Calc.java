package com.gtf.test;

public class Calc {

	static public double add(double firstNum, double SecondNum) {
		return firstNum + SecondNum;
	}
	
	static public double sub(double firstNum, double SecondNum) {
		return firstNum - SecondNum;
	}
	
	static public double mul(double firstNum, double SecondNum) {
		return firstNum * SecondNum;
	}
	
	static public double div(double firstNum, double SecondNum) {
		return firstNum / SecondNum;
	}
	
}
